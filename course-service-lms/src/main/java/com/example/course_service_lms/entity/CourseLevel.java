package com.example.course_service_lms.entity;

/**
 * Enumeration representing the difficulty level of a course.
 * Used to categorize courses based on the intended learner's experience level.
 */
public enum CourseLevel {

        /** Indicates that the course is intended for beginners with little to no prior knowledge. */
        BEGINNER,

        /** Indicates that the course is for learners with some foundational knowledge. */
        INTERMEDIATE,

        /** Indicates that the course is for advanced learners or professionals. */
        PROFESSIONAL
}


